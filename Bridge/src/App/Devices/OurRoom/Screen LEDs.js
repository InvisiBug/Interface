client.on("message", (topic, payload) => {
  if (topic == "Screen LEDs") {
    if (payload != "Screen LEDs Disconnected") {
      screenLEDs = JSON.parse(payload);
    } else {
      screenLEDs = null;
      console.log("Screen LEDs Disconnected at " + functions.printTime());
    }
  }
});

